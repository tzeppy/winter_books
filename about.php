    <h2><center>FAQ</center></h2>
<p class="faq">
Q: What are your books all about?<br>
A: My books are about challenges we all face, inside and outside us, and suggestions on how to overcome them.
Also they are about friendship, sacrifice, finding your place in life, and, of course, love.
</p>
<br>
<p class="faq">
Q: Why seven books?<br>
A: Each book is a school year of my main characters' life. Two years of high school plus five years of universy equals seven. 's a structure similar to Harry Potter books.
</p>
<br>
<p class="faq">
Q: Why did you chose Winter as your pen name?<br>
A: I was born in mid-December in St Petersburg. Not the one in Florida but in Russia, a stone's throw away from the Polar Circle.
So my life began with a serious Winter and I relate to it as my cradle.
My early life in Russia was winter-cold and harsh, but it made me who I am. I am grateful for that metaphorical Winter too.
And last but not the least - Winter is my husband's middle name. So Anna Winter it is.
</p>
<br>
<p class="faq">
Q: How long would it take you to finish all seven books?<br>
A: I don't know. As I see  now, each book is going to be about seven hundred pages.
I am planning to release them in parts though, with around three hundred pages each.
The time gap between the parts will be a couple of months; between the books - longer.
I'll do my best not to keep my readers waiting a long time for the next installment.
Still, it takes time, but would you wait a bit and get a great book, or would you prefer less wait and a disappointing read?
Life is all about trade-offs, isn't it?
</p>
<br>
If you have more questions - write to me. I'll try to find time to answer.

