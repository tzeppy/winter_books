<p>
<i>Multiverse Time Travel Institute</i> is a series of seven science fiction books that chronicle the adventures
of a young girl, Ona Bell, as she is studying time travel techniques at a special school - VERS. 
Each book comprises a school year and is named after the color of the year's uniform.
<i>Carmine</i> and <i>Perinone</i> are the two high school years.
<i>Orpiment, Viridian, Turquoise, Indigo,</i> and <i>Amethyst</i> are the university years.
The series is a blend of several genres - science fiction, coming of age, and boarding school, with elements of mystery, adventure, thriller, and romance.</p>
<p>
The main story arc concerns Ona's struggles to overcome problems associated with time travel abilities and her quest to overcome Exzellenz, the evil leader of the dark time travelers.</p>
<p>
The first installment in the  series, Carmine, begins with Ona's arrival to California to take the entrance exam for the new high school.</p>
<p>
Ona is a sixteen year old Lithuanian who grew up in a family of gifted musicians moving between metro cities
in different countries.
She yearns to find her own place in the world, to make a difference;
but unmusical and friendless, she feels like a useless outcast.
</p>
<p>
She is good at drawing and painting and receives an invitation to apply to an innovative California school specializing in art.
It turns out to be VERS, a division of the Multiverse Time Travel Institute (MTTI) - a secret community of people with a rare
multi-genetic trait that enables them to travel to parallel universes,
but only to the segments that are identical to our universe in the past, which in effect makes it Time Travel.
</p>
<p>Artistic inclinations are a part of the Time Travelers' multigenetic trait, so most are talented musicians, painters
and multi-media artists, some famous in the 'Outside World'. Art education is also important because MTTI uses Time Travel
to study the evolution of human society as reflected in art.
The Founders of MTTI firmly believe that comprehension of art as an expression of a collective historical thought
is vital to bringing the changes needed for the survival of humans.</p>
<p>
At VERS, a sophisticated artistic institution that consists of a high school and a university with its different
research facilies, Ona finally finds a place she feels she belongs.
She desperately wants to succeed, as an artist and a time traveller, but becoming an Active Timer is a daunting task.
Time Travel is taxing on human health.
Its intensity must be appropriate for the body's ability to recover, or a Timer's health will quickly deteriorate.
In addion to the complicated techniques necessary for Time Travel the students have to learn how to maintain
a good physical and mental form.
Mastering all of that requires a high level of self-control, which is hard to learn by itself.
</p>
<p>
So Ona begins her journey of self-exploration to find the empowerment that can only come from overcoming formidable resistance,
both external and internal.
Her companions on the journey are Elara, a serene African girl who becomes her best friend;
Oz, a sentimental son of distinguished Timers from the UK;
Raj, a sarcastic Indian-Californian clown;
Jing, a very determined Chinese pragmatic;
Samantha, an easy-going Australian tomboy;
Agung, a sweet Balinese dreamer;
Leandro, a thuggish-looking enigmatic Latino;
Clemency - an insecure yoga girl from Santa Cruz, CA; and Ransom, a pain-in-the-neck New Yorker.
</p>
